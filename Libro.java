
public class Libro
{
    private String Titulo;
    private String Autor;
    public Libro(String Titule, String Autore)
    {
        Titulo=Titule;
        Autor=Autore;
    }
    public String dimeAutor()
    {
       return Autor;
    }
    public String dimeTitulo()
    {
       return Titulo;
    }
}
